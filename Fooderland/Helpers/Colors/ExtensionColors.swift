import Foundation
import UIKit

extension UIColor {
    
    static var cellBackgroundColor: UIColor {
        return UIColor(named: "cellBackgroundColor") ?? .white
    }
    
    static var viewBackgroundColor: UIColor {
        return UIColor(named: "viewBackgroundColor") ?? .white
    }
    
}
